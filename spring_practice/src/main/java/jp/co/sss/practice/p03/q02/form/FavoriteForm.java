package jp.co.sss.practice.p03.q02.form;

public class FavoriteForm {

	String fruitString;
	String snack;
	String sweet;
	String sidedish;

	/**
	 * @return fruitString
	 */
	public String getFruitString() {
		return fruitString;
	}

	/**
	 * @param fruitString セットする fruitString
	 */
	public void setFruitString(String fruitString) {
		this.fruitString = fruitString;
	}

	/**
	 * @return snack
	 */
	public String getSnack() {
		return snack;
	}

	/**
	 * @param snack セットする snack
	 */
	public void setSnack(String snack) {
		this.snack = snack;
	}

	/**
	 * @return sweet
	 */
	public String getSweet() {
		return sweet;
	}

	/**
	 * @param sweet セットする sweet
	 */
	public void setSweet(String sweet) {
		this.sweet = sweet;
	}

	/**
	 * @return sidedish
	 */
	public String getSidedish() {
		return sidedish;
	}

	/**
	 * @param sidedish セットする sidedish
	 */
	public void setSidedish(String sidedish) {
		this.sidedish = sidedish;
	}

}
